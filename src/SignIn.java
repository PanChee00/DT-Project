    private void syndesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_syndesiActionPerformed
        // TODO add your handling code here:
        try{
            dbConnection = DriverManager.getConnection(url,username,passwd);
            ps = dbConnection.prepareStatement("SELECT username,passwd,role FROM users WHERE username = ? AND passwd = ?");
            ps.setString(1, username_input.getText());
            ps.setString(2, String.valueOf(password_input.getPassword()));
            
            
            ResultSet res = ps.executeQuery();
            if(res.next()){
                String role = res.getString("role");
                System.out.println(role);
                output.setText("Login Successful");
                output.setForeground(Color.green);
                if(role.equals("user")){
                        WelcomeUserFrame.setVisible(true);
                        WelcomeUserFrame.setSize(350,500);
                        welcome_text_user.setText("Welcome, "+username_input.getText());
                    }else{
                        WelcomeAdminFrame.setVisible(true);
                        WelcomeAdminFrame.setSize(350,500);
                        welcome_text_admin.setText("Welcome, "+username_input.getText());
                    }   
            }else{
                output.setText("Invalid username or password");
                output.setForeground(Color.red);
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            System.out.println(ex.getSQLState());
            System.out.println(ex.getErrorCode());
        }
    }//GEN-LAST:event_syndesiActionPerformed

